#ifndef STR_VIEW_H
#define STR_VIEW_H

#include <string>

//=======================================================================================
namespace s11n {
namespace impl
{
    //===================================================================================
    struct str_view
    {
        const char *ptr;
        size_t len;

        std::string str() const
        {
            return {ptr, len};
        }
    };
    //===================================================================================
} // namespace impl
} // namespace s11n
//=======================================================================================

#endif // STR_VIEW_H
